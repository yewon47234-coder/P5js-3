function setup() {
  createCanvas(600, 400);
  noLoop(); // 한 번만 그림
}

function draw() {
  background(220);

  // ===== 중앙 과녁 =====
  fill(0, 0, 255); ellipse(300, 200, 400, 400); // 파랑
  fill(255, 0, 0); ellipse(300, 200, 320, 320); // 빨강
  fill(255, 255, 0); ellipse(300, 200, 240, 240); // 노랑
  fill(0); ellipse(300, 200, 160, 160);          // 검정
  fill(255); ellipse(300, 200, 80, 80);          // 흰색

  // ===== 삼각형 =====
  fill(0, 255, 0, 180);
  triangle(50, 50, 200, 50, 125, 150);

  fill(255, 0, 150, 180);
  triangle(400, 20, 550, 100, 350, 120);

  // ===== 사변형 (quad) =====
  fill(200, 100, 100, 180);
  quad(50, 250, 200, 230, 180, 350, 40, 360);

  fill(0, 200, 200, 180);
  quad(400, 280, 560, 260, 520, 370, 380, 360);

  // ===== 원 =====
  fill(255, 150, 0, 200);
  ellipse(100, 300, 100, 100);

  fill(150, 0, 255, 200);
  ellipse(500, 80, 120, 120);

  // ===== 아크 =====
  fill(128);
  arc(150, 200, 100, 100, 0, radians(270));

  fill(255);
  arc(450, 200, 100, 100, radians(90), radians(360));
}
